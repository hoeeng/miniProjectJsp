package miniLikey;

public class MiniLikeyDTO {
	
	private String id;
	private int num, datnum, likey;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getDatnum() {
		return datnum;
	}
	public void setDatnum(int datnum) {
		this.datnum = datnum;
	}
	public int getLikey() {
		return likey;
	}
	public void setLikey(int likey) {
		this.likey = likey;
	}
	
	

}
